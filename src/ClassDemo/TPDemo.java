package ClassDemo;

public class TPDemo {
	private int id ;
	private int mimute ;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getMimute() {
		return mimute;
	}
	public void setMimute(int mimute) {
		this.mimute = mimute;
	}
	@Override
	public String toString() {
		return "TPDemo [id=" + id + ", mimute=" + mimute + "]";
	}
	

}
