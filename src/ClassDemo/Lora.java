package ClassDemo;

public class Lora {
	private int id ;
	private int tp;
	private int hd;
	private int co2;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getTp() {
		return tp;
	}
	public void setTp(int tp) {
		this.tp = tp;
	}
	public int getHd() {
		return hd;
	}
	public void setHd(int hd) {
		this.hd = hd;
	}
	public int getCo2() {
		return co2;
	}
	public void setCo2(int co2) {
		this.co2 = co2;
	}
	public String toString() {
		return "Lora [id=" + id + ", tp=" + tp + ", hd=" + hd + ", co2=" + co2
				+ "]";
	}
	

}
